---find the user who book first hotel 
with cte as (
SELECT *, ROW_NUMBER() OVER (PARTITION BY user_id ORDER BY user_id,booking_date) as row_num
FROM booking_table)
select * from cte 
where row_num = 1 and line_of_business = 'Hotel'

