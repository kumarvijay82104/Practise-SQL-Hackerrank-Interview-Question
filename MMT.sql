from booking_table group by 1 order by 1

--write a query to count the number of flight and hotel booking in each of the user segments for the year 2022


SELECT segment, COUNT(*) FILTER (WHERE line_of_business = 'Flight') No_of_flight_booking, 
COUNT(*) FILTER (WHERE line_of_business = 'Hotel') No_of_Hotel_booking
FROM booking_table b join user_table u on u.user_id = b.user_id
where booking_date between '2022-01-01' and '2022-12-31'
GROUP BY segment order by segment
